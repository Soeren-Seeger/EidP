
public class Messdatum {

	private int u = 0;
	private double t = 0;
	private double d = 0;
	

	Messdatum (int u, double t, double d) {

		this.u = u;
		this.t = t;
		this.d = d;
			
	}
	
	public int getU() {
		return u;
	}
	
	public double getT() {
		return t;
	}
	
	public double getD() {
		return d;
	}

}
